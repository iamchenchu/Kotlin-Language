// in kotlin, the type of variable is decided by it's value

fun main() {

    val myNum = 5 // Int
    val myDubleNum = 5.99 // Float
    val myLetter = 'R' // Char
    val myBoolean = true // Boolean
    val myText = "Hello" // String

    println(myNum)
    println(myDubleNum)
    println(myLetter)
    println(myBoolean)
    println(myText)
    val myNum1: Short = 5000
    println(myNum1)
}
